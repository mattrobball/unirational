#!/usr/bin/env python3
"""Exact linear-algebra checks for conic_factor_theorem.md."""

from __future__ import annotations

import itertools

import sympy as sp


s, t = sp.symbols("s t")
x0, x1, x2, y0, y1, y2 = sp.symbols("x0 x1 x2 y0 y1 y2")


def binary_basis(degree: int) -> list[sp.Expr]:
    return [s**i * t ** (degree - i) for i in range(degree + 1)]


def binary_rank(columns: list[sp.Expr], target_degree: int) -> int:
    polys = [sp.Poly(sp.expand(f), s, t, domain=sp.QQ) for f in columns]
    rows = []
    for monomial in binary_basis(target_degree):
        power = sp.Poly(monomial, s, t).monoms()[0]
        rows.append([f.coeff_monomial(power) for f in polys])
    return int(sp.Matrix(rows).rank())


def ternary_basis(vars_: tuple[sp.Symbol, ...], degree: int) -> list[sp.Expr]:
    ans = []
    for exponents in itertools.product(range(degree + 1), repeat=len(vars_)):
        if sum(exponents) == degree:
            monomial = sp.Integer(1)
            for variable, exponent in zip(vars_, exponents):
                monomial *= variable**exponent
            ans.append(monomial)
    return ans


def bidegree_basis(dx: int, dy: int) -> list[sp.Expr]:
    return [
        mx * my
        for mx in ternary_basis((x0, x1, x2), dx)
        for my in ternary_basis((y0, y1, y2), dy)
    ]


def span_rank(columns: list[sp.Expr], ambient: list[sp.Expr]) -> int:
    variables = (x0, x1, x2, y0, y1, y2)
    monomials = [sp.Poly(m, *variables).monoms()[0] for m in ambient]
    polys = [sp.Poly(sp.expand(f), *variables, domain=sp.QQ) for f in columns]
    matrix = [[f.coeff_monomial(m) for f in polys] for m in monomials]
    return int(sp.Matrix(matrix).rank())


def quotient_dimension(H: sp.Expr, C: sp.Expr) -> int:
    ambient = bidegree_basis(2, 3)
    columns = (
        [H * m for m in bidegree_basis(1, 2)]
        + [C * m for m in bidegree_basis(2, 1)]
    )
    return len(ambient) - span_rank(columns, ambient)


def main() -> None:
    H3 = x0 * y0 + x1 * y1 + x2 * y2
    H2 = x0 * y0 + x1 * y1
    conic_through = y0 * y2 - y1**2
    conic_away = y0 * y1 - y2**2

    assert quotient_dimension(H3, conic_through) == 27
    assert quotient_dimension(H2, conic_away) == 27
    print("rank-3 and rank-2-away reduced-conic quotient dimensions: 27")

    forms6 = binary_basis(6)

    # Rank two, conic avoiding b: the pencil is generated by s^2,t^2.
    l0, l1 = s**2, t**2
    rank_square = binary_rank(
        [l1**2 * f for f in forms6]
        + [-2 * l0 * l1 * f for f in forms6]
        + [l0**2 * f for f in forms6],
        10,
    )
    rank_mixed = binary_rank(
        [l1 * f for f in forms6] + [-l0 * f for f in forms6], 8
    )
    assert (rank_square, rank_mixed, len(forms6)) == (11, 9, 7)
    print("rank-2 away component ranks: 11 + 9 + 7 = 27")

    # Rank two, conic through b: remove the common factor, leaving s,t.
    l0, l1 = s, t
    rank_square = binary_rank(
        [l1**2 * f for f in forms6]
        + [-2 * l0 * l1 * f for f in forms6]
        + [l0**2 * f for f in forms6],
        8,
    )
    rank_mixed = binary_rank(
        [l1 * f for f in forms6] + [-l0 * f for f in forms6], 7
    )
    assert (rank_square, rank_mixed, len(forms6)) == (9, 8, 7)
    print("rank-2 through-basepoint component ranks: 9 + 8 + 7 = 24")

    # Affine determinant-zero dimensions from the exhaustive UFD counts.
    assert (27 - 10, 27 - 11, 24 - 9) == (17, 16, 15)
    print("determinant-zero codimensions: 17, 16, 15")
    print("all conic-factor local checks passed")


if __name__ == "__main__":
    main()

